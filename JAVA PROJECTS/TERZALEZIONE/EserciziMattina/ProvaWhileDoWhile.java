package TERZALEZIONE.EserciziMattina;

public class ProvaWhileDoWhile {
    
    public static void main(String[] args){

        int i = 1;
        
        while(i <= 5){
            i++;
            System.out.println(i);
        }

        do{
            i++;
            System.out.println(i);
        }while(i <= 5);
    }
}
